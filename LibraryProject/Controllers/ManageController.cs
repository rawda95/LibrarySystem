        // GET: /Manage/Index
        public ActionResult Index(string id)
        {

            dynamic model, hireDate = DateTime.MaxValue, salary = double.MaxValue;
            ApplicationDbContext db = new ApplicationDbContext();
            if (!User.IsInRole("Member"))
            {

                model = db.Users.Include(a => a.EmployeeModel).SingleOrDefault(p => p.Id == id);
                hireDate = model.EmployeeModel.HireDate;
                salary = model.EmployeeModel.Salary;


            }
            else
            {
                model = db.Users.SingleOrDefault(p => p.Id == id);


            }
            string image = Convert.ToBase64String(model.Photo);
            ViewBag.imgSrc = string.Format("data:0; base64, {1}", MimeMapping.GetMimeMapping(image), image);

            RegisterViewModel registerViewModel = new RegisterViewModel()
            {
                UserName = model.UserName,
                Email = model.Email,
                FirstName = model.FirstName,
                LastName = model.LastName,
                Address = model.Address,
                Phone = model.PhoneNumber,
                HireDate = hireDate,
                Salary = salary,


            };
            return View(registerViewModel);

        }



        //
        // POST: /Manage/RemoveLogin
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> RemoveLogin(string loginProvider, string providerKey)
        {
            ManageMessageId? message;
            var result = await _userManager.RemoveLoginAsync(User.Identity.GetUserId(), new UserLoginInfo(loginProvider, providerKey));
            if (result.Succeeded)
            {
                var user = await _userManager.FindByIdAsync(User.Identity.GetUserId());
                if (user != null)
                {
                    await SignInAsync(user, isPersistent: false);
                }
                message = ManageMessageId.RemoveLoginSuccess;
            }
            else
            {
                message = ManageMessageId.Error;
            }
            return RedirectToAction("ManageLogins", new { Message = message });
        }

